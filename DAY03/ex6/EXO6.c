#include <stdio.h>


void ft_swap(int *a, int*b)
{
  printf("les valeurs de bases sont : %i et %i \n", *a, *b);
  int tmp =0;
  tmp = *a;
  *a = *b;
  *b = tmp;
  printf("les nouvelles valeurs sont : %i et %i", *a, *b);
  return;
}




int main ()
{
  int *nb1;
  int *nb2;
  int a;
  int b;

  a = 34;
  b = 43;

  nb1 = &a;
  nb2 =&b;
  
  ft_swap(nb1,nb2);
  printf("\n");
  return (0) ;
}
